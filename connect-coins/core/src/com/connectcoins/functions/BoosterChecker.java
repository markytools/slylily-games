package com.connectcoins.functions;

public class BoosterChecker {
	public boolean hasFireUp;
	public boolean hasShineAllTheWay;
	public boolean hasLuckyHalf;
	public boolean hasLuckyThird;
	
	public BoosterChecker(boolean hasFireUp, boolean hasShineAllTheWay,
			boolean hasLuckyHalf, boolean hasLuckyThird) {
		super();
		this.hasFireUp = hasFireUp;
		this.hasShineAllTheWay = hasShineAllTheWay;
		this.hasLuckyHalf = hasLuckyHalf;
		this.hasLuckyThird = hasLuckyThird;
	}
}
